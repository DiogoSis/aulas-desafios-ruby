#escopo: define onde uma variável estará disponível dentro do seu programa
# 4 tipo de escopo:
# 1. Local
# 2. Global
# 3. Instância
# 4. Classe

#local - podem ser acessadas apenas dentro do bloco onde foram definidas
#formato - primeira letra minúscula ou _

#global - podem ser acessadas em qualquer lugar do programa
#formato - primeira letra $

#instância - podem ser acessadas por qualquer método de uma classe
#formato - primeira letra @

#classe - podem ser acessadas por qualquer método de uma classe
#formato - primeira letra @@